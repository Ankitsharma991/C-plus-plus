// ***** What is Inheritance in C++ ********** 
/*
1.The concept of Reusability in C++ is supported using Inheritance
2.We can reuse the properties of an existing class by inheritance from it
3.The existing class is called Base Class
4.The new class which is inherited is called as the Derived Class
5.Reusing classes saves time and money
6.There are different types of inheritance in C++
*/

// --------------------------------------------------------------------------------
/* ************** Forms of inheritance in C++ *************
0.Single Inheritance :- A derived class with only one Base Class
1. Multiple Inheritance :- A Derived class with more than one Base Class
2.Hierarchical Inheritance :- Several Derived classes from single Base Class
3.Multi-level Inheritance   :- A Derived class from already Derived Class 
4.Hybrid Inheritance  :-Hybrid Inheritance is a combination of multiple inheritance and multi-level  inheritance 
            --> A class is derived from two classes as in multiple inheritance
            --> However, one of the parents classes is not a base class  
*/
